                <h2 class="text-uppercase">Cara memilih cushion compact</h2>
                <p class="item-intro text-muted">“Makeup is not a mask that covers up your beauty, 
              it’s a weapon that helps you express who you are from the inside.”</p>
                <img class="img-fluid d-block mx-auto mt-4 mb-4" style="width: 50%" src="assets/img/about/mu24.jpg" alt="" />
            <p><p align ="justify">Baik cushion berjenis drugstore makeup ataupun high-end makeup, sangat penting memilih produk berdasarkan kebutuhan. 
            Rentetan elemen perlu diperhatikan, seperti warna dasar kulit, tekstur cushion, level coverage, bahan aktif, efek akhir, dan formula penutrisi maupun pelindung kulit yang terkandung dari setiap produk. 
            Mari kupas satu per satu aspek dalam memilih cushion tersebut di section ini!</p>
        <p><p align ="justify"><b>1.Identifikasi Jenis dan Undertone Kulit Wajah Anda</b>
        <img class="img-fluid d-block mx-auto mt-4 mb-4" style="width: 50%" src="assets/img/about/mu25.jpg" alt="" />
        <p><p align ="justify">Hal pertama yang mesti dilakukan saat memilih cushion adalah menyesuaikan shade cushion dengan warna dasar (undertone) kulit wajah Anda, serta jangan pertimbangkan juga apakah cushion tersebut meng-cover dengan baik untuk jenis kulit Anda. 
        Oleh karena cushion merupakan kosmetik yang “lahir” di Korea, tentu kebanyakan produk mempunyai varian warna selaras dengan kulit perempuan Korea yang cenderung putih.</p>
        <p><p align ="justify">Untuk kulit orang Indonesia yang rata-rata ber-undertone kuning langsat, pastikan Anda mencari warna cushion yang sesuai. 
        Jika berbeda, kulit wajah bisa terlihat abu-abu (greyish), dan bahkan kusam.  
        Anda bisa mengutamakan produk lokal yang variasi shade-nya didesain khusus kebutuhan orang Indonesia. 
        Walau demikian, cushion non-lokal tetap bisa Anda pilih, asalkan warnaya cocok dengan undertone kulit Anda.</p>
        <p><p align ="justify"><b>2.Pilihlah Cushion berdasarkan Tekstur dan Daya Coverage-nya</b>
            <img class="img-fluid d-block mx-auto mt-4 mb-4" style="width: 50%" src="assets/img/about/mu26.jpg" alt="" />
            <br> Tekstur cushion compact secara umum terdiri dari 2 bentuk, yakni tekstur mirip foundation cair dan lainnya menyerupai tekstur foundation krim. 
            Untuk style makeup tipis dan natural, pilihlah cushion bertekstur cair. 
            Formula ringan pada cushion compact ini mudah menyatu ke kulit serta tidak membuat wajah terlihat cakey. 
            Anda juga bisa memaksimalkan riasan dengan pemberian concealer.</p>
            <p><p align ="justify">Di sisi lain, untuk memperlihatkan makeup flawless tanpa bekas jerawat, flek hitam, dan imperfeksi lain bisa menggunakan cushion yang teksturnya menyerupai foundation krim. 
            Daya coverage yang ditawarkan cushion compact foundation berwujud krim lebih dalam. 
            Untuk cushion ber-coverage tinggi sebaiknya tidak perlu lagi menerapkan concealer untuk mencegah heavy makeup.</p>    
        <p><p align ="justify"><b>3.Sesuaikan Kandungan Formula dengan Kondisi Kulit</b>
            <img class="img-fluid d-block mx-auto mt-4 mb-4" style="width: 50%" src="assets/img/about/mu27.jpg" alt="" />
            <p><p align ="justify">Komponen dalam tabel ingredients penting Anda teliti sewaktu ingin membeli cushion. 
            Kandungan pengontrol minyak, pelembap, dan zat aditif perlu diperiksa keberadaannya agar Anda mendapatkan petunjuk menemukan produk yang tepat sesuai kondisi kulit.</p>
            
        <p><p align ="justify"><b>4.Tips untuk Kulit Berminyak,Kering dan Sensitif</b>
            <img class="img-fluid d-block mx-auto mt-4 mb-4" style="width: 50%" src="assets/img/about/mu28.jpg" alt="" />
            <p><p align ="justify"><b>Untuk Kulit Berminyak, Pilih Cushion yang Mengandung Oil Absorber</b>
            <br>Apabila Anda mencari cushion compact untuk kulit berminyak, periksalah kehadiran formula oil control dari setiap produk. 
            Green tea merupakan salah satu contoh agen yang efektif menahan pengeluaran minyak berlebih. 
            Dengan demikian, makeup pun tidak mudah luntur akibat sebum yang tak terkontrol.</p>
        <p><p align ="justify">Jika Anda merasa kesulitan memahami product ingredients, aspek lain yang lebih mudah untuk diperhatikan adalah mengecek klaimnya. 
        Jika tercantum keterangan “water-resistant” atau “with mattifying formula”, maka klaim tersebut menjadi hint untuk Anda. 
        Selain itu, cushion berbahan dasar air juga dapat dipilih untuk Anda yang kulitnya rentan berminyak</p>
        <p><p align ="justify"><b>Untuk Kulit Kering, Pilih Cushion yang Mengandung Pelembap</b>    
        <br>Apakah Anda memiliki kulit kering dan khawatir makeup cracky karena salah mengenakan makeup base? Salah satu tips untuk mencegah trouble tersebut adalah memilih produk yang dapat memberikan kelembapan kulit. 
        Saat ini, cushion compact untuk kulit kering banyak dilengkapi komponen pelembap seperti hyaluronic acid, collagen, ceramide, atau moisturizing agents lain.</p>
        <p><p align ="justify">Cushion compact water-based dapat pula menjadi opsi terbaik untuk Anda yang berkulit kering. 
        Periksa juga apakah produk tersebut diperlengkap oleh pelembap alami, misalnya ekstrak blueberry dan aloe vera yang membebaskan kulit dari kekeringan. 
        Cushion dengan klaim memberikan cooling sensation juga ideal Anda pilih agar kulit kering tetap segar.</p>
        <p><p align ="justify"><b>Untuk Kulit Sensitif, Pilih Cushion yang Tidak Mengandung Zat Aditif</b> 
        <br>Penggunaan makeup terlalu lama cukup berisiko bagi kulit wajah sensitif, terlebih jika produk kosmetik yang diterapkan banyak menyimpan kandungan bahan kimia. 
        Untuk mencegah terjadinya iritasi,Anda yang berkulit sensitif perlu menghindari cushion berkandungan zat aditif.</p> 
         <p><p align ="justify">Carilah cushion tanpa paraben, mineral oil, alkohol, pewarna, artificial fragrance, ataupun pengawet lain agar memperkecil efek iritasi. 
         Sebaiknya Anda melakukan percobaan produk yang telah dibeli terlebih dahulu untuk memastikan kecocokannya dengan kulit.</p>   
        <p><p align ="justify"><b>5.Tentukan Cushion berdasarkan Hasil Akhir yang Anda Inginkan</b>
        <img class="img-fluid d-block mx-auto mt-4 mb-4" style="width: 50%" src="assets/img/about/mu29.jpg" alt="" />
        <p><p align ="justify">Di samping mempertimbangkan cushion berdasarkan undertone, tekstur, level coverage, maupun formulanya, periksa juga apakah produk tersebut memberikan hasil akhir sesuai tipe kulit Anda. 
        Bagi kulit kering, cushion yang mampu menampilkan hasil akhir dewy patut diberikan ‘lampu hijau’ karena makeup look dewy finish membuat wajah Anda terlihat berkilau.</p> 
         <p><p align ="justify">Sedangkan untuk kondisi kulit rentan berminyak, kami sarankan Anda mencari cushion yang bekerja efektif menyerap sebum. 
         Cushion compact yang cocok untuk kulit berminyak biasanya menawarkan matte finish makeup look, di mana kulit wajah tampak seperti porselen.</p>      
        <p><p align ="justify"><b>6.Pertimbangkan Produk yang Dilengkapi Formula SPF</b>
        <img class="img-fluid d-block mx-auto mt-4 mb-4" style="width: 50%" src="assets/img/about/mu30.jpg" alt="" />
        <p><p align ="justify">Cuaca panas dapat membuat kulit gosong akibat produksi melanin yang out of control. 
        Apabila Anda ingin melindungi kulit wajah tanpa pengaplikasian tabir surya, cari saja cushion yang mengandung SPF sehingga tidak butuh membuat banyak layer di kulit wajah.</p> 
        <p><p align ="justify">Formula tabir surya pada cushion akan melindungi kulit dari dampak buruk sinar matahari. 
        Cushion yang dilengkapi SPF juga bertindak sebagai anti-aging yang menghambat pembentukan age spot, seperti keriput dan garis halus, 
        serta mencegah masalah lain semacam bintik hitam, kulit kasar, warna kulit tidak merata, atau bahkan kanker kulit akibat paparan sinar ultraviolet.</p>
