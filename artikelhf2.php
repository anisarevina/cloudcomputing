            <h2 class="text-uppercase">Tips untuk makan makanan sehat bagi orang yang sibuk</h2>
            <p class="item-intro text-muted">Health start from ourselves.</p>
            <img class="img-fluid d-block mx-auto mt-4 mb-4" style="width: 50%" src="assets/img/portfolio/ygt2.jpg" alt="" />
        <p align ="justify">Dikejar deadline, padatnya jadwal meeting, lembur sampai malam, harus mengurus rumah, dan kesibukan lainnya terkadang bikin orang-orang yang sibuk susah mengontrol asupan makanan. 
            Biasanya, mereka hanya memilih makan yang cepat dan gampang diperoleh supaya tidak menyita banyak waktu.
        Padahal, asupan makanan sehat adalah hal yang penting untuk menunjang kesehatan manusia, mengendalikan berat badan, meningkatkan kinerja otak, dan menambah tenaga seharian. Berikut adalah tips dan pilhan makanan sehat yang gampang diperoleh untuk orang-orang sibuk.<br/>
    <p><p align ="justify"><b>1.Sarapan</b>
        <br>Sarapan sangat penting untuk menyokong energi dan membantu mengendalikan gula darah. Hindari sarapan dengan makanan yang mengandung karbohidrat sederhana dan gula. Menu makanan sehat saat sarapan yang dianjurkan untuk disantap:
    <br>- Makanan dengan karbohidrat kompleks yang tinggi misalnya oatmeal, nasi merah, sereal, buah, dan sayur.
    <br>- Makanan yang mengandung serat yang tinggi misalnya sereal gandum dan chia seed.
    <br>- Menu yang kaya akan protein misalnya kacang-kacangan, biji-bijian, susu almond, susu sapi rendah lemak, yoghurt, dan telur.
    <br>- Makanan dengan lemak baik dari kacang-kacangan, biji-bijian, alpukat, dan kelapa. </p>
    <p><p align ="justify"><b>2.Jangan kebanyakan minum kopi</b>
        <br>Minum kopi memberikan manfaat untuk kita, tapi jangan sampai berlebihan. Kebanyakan minum kopi akan membebani organ hati, 
        membuat Anda dehidrasi (karena kopi adalah minuman yang bersifat diuretik alias meningkatkan kecepatan pembentukan urine).
    <br>Oleh karena itu, penting untuk memperhatikan asupan kopi. 
    Usahakan untuk tidak minum kopi lebih dari 400 mg per hari agar tetap aman dan tidak memberikan efek samping yang berbahaya. Sebagai alternatif bila butuh asupan kafein, 
    Anda dapat meminum teh herbal seperti misalnya teh hijau atau matcha yang juga digandrungi semua orang.</p>
    <p><p align ="justify"><b>3.Perbanyak minum air putih</b>
        <br>Jangan lupa untuk banyak minum. Minimal minumah 8 gelas air putih dalam sehari agar kadar energi Anda tetap tinggi, pencernaan Anda lancar, dan konsentrasi Anda terjaga.
        <br>Ketika tubuh Anda dehidrasi, terkadang tubuh Anda malah akan memberikan sinyal yang yang keliru, yaitu sensasi lapar. Anda bisa minum air putih 20 menit sebelum atau 60 menit sesudah makan. </p>
    <p><p align ="justify"><b>4.Kurangi makan makanan kemasan</b>
        <br>Pada umumnya, makanan kemasan mengandung banyak gula, garam, penstabil, pengawet, perasa buatan, pewarna buatan, dan lain sebagainya. Bahan-bahan makanan tersebut kurang baik bagi kesehatan. 
        Untuk lebih banyak mengasup makanan sehat, hindari makan makanan kemasan dan makanan siap saji.</p>
        <p><p align ="justify"><b>5.Makan makanan yang segar</b>
        <br>Daging, ikan, kacang-kacangan, gandum, buah, dan sayur yang segar adalah makanan sehat yang baik untuk kesehatan. Jika Anda ingin membawa snack, bawalah kacang-kacangan atau buah dan sayur yang dipotong kecil-kecil.</p>    
        <p><p align ="justify"><b>6.Perhatikan hasil proses makanan di restoran</b>
        <br>Waktu memesan makanan di restoran, perhatikan proses pembuatan makanan di buku menu, apakah makanan itu dikukus, direbus, dipanggang, atau dibakar. Cobalah untuk hindari makanan yang digoreng, 
            makanan yang dimasak kering dengan panas yang tinggi atau ditambahi dengan minyak atau lemak, atau au gratin yang mana makanan Anda ditaburi keju yang dipanaskan di atasnya.
        <br>Makanan-makanan dengan proses pengolahan seperti itu mengandung lemak dan kalori yang tinggi. Lebih baik Anda memilih menu makanan sehat yang direbus atau dipanggang. </p>    
        <p><p align ="justify"><b>7.Makan makanan sehat tinggi serat</b>
        <br>Daripada makan nasi putih, pilih nasi merah yang lebih kaya kandungan seratnya. Anda juga bisa mengganti roti tawar putih yang biasa Anda makan dengan roti gandum. Selain lebih tinggi serat, roti gandum dan nasi merah 
        juga membuat Anda kenyang lebih lama sehingga bisa beraktivitas seharian dengan lancar.</p>    
        <p><p align ="justify"><b>8.Bawalah snack sehat sendiri</b>
        <br>Ke mana pun Anda pergi, bawalah camilan sehat seperti buah yang dipotong kecil-kecil, jus, oatmeal, yogurt, sayur segar yang dipotong kecil-kecil, atau kacang-kacangan. Makanlah snack Anda setiap 3 jam sekali 
        untuk membantu gula darah Anda tetap stabil dan mencegah Anda makan berlebihan saat jam makan.</p>   
        <p><p align ="justify"><b>9.Jangan makan berlebihan</b>
        <br>Makanlah secukupnya saat jam makan, yaitu sekitar 80 persen kenyang dan jangan sampai lebih dari itu. Kalau Anda masih merasa lapar 10 menit kemudian, barulah makan sedikit lagi.
        <br>Makan berlebihan bukan saja mengakibatkan bertambahnya berat badan, melainkan juga menghambat sistem pencernaan Anda dan dapat mengakibatkan penyakit-penyakit yang lebih serius seperti diabetes. 
        Ya, meskipun Anda makan makanan sehat, porsinya tetap harus dikendalikan. </p>    
        <p><p align ="justify"><b>10.Perbanyak makan buah dan sayur</b>
        <br>Makan banyak buah dan sayur setiap harinya. Carilah buah dan sayur yang penuh warna, segar, dan jangan lupa dicuci terlebih dahulu untuk menghilangkan zat-zat kimia atau pestisida 
        yang mungkin terdapat pada buah dan sayur tersebut.
        <br>Pilihlah sayur-sayuran hijau, seperti kangkung, bayam, atau brokoli. Sayur-sayuran hijau banyak mengandung vitamin serta mineral yang bermanfaat bagi kesehatan.</p>    
        <p><p align ="justify"><b>11.Makan secara teratur dan tepat waktu</b>
        <br>Laju metabolisme Anda akan melambat saat malam. Dianjurkan untuk makan makanan sehat sebelum jam 8 malam agar tubuh Anda sempat untuk memproses makanan yang Anda makan 
        dan membersihkan racun pada tubuh Anda sehingga Anda terhindar dari berat badan berlebih. Usahakan untuk selalu makan secara teratur, ya. </p>    
        <p><p align ="justify"><b>12.Konsumsi suplemen</b>
        <br>Terlepas dari sebarapa baik asupan makanan sehat yang Anda makan, tidak ada seorang pun yang memiliki nutrisi sempurna pada tubuh. Dengan lingkungan yang mengandung banyak zat-zat yang tidak baik bagi tubuh kita, 
        atau stres karena pekerjaan dapat mempengaruhi nutrisi di dalam tubuh kita. Berikut adalah suplemen-suplemen yang dianjurkan untuk dikonsumsi:
        <br>- Suplemen multivitamin atau mineral
        <br>- Suplemen probiotik
        <br>- Vitamin B kompleks (untuk membantu mengurangi stres, meningkatkan kerja metabolisme dan membantu meningkatkan sistem imunitas)
        <br>- Suplemen yang mengandung asam lemak esensial om ega-3 atau omega-6
        <br>- Minyak ikan (untuk membantu meningkatkan kerja otak, meningkatkan sistem pencernaan, mengurangi stres, dan mengontrol nafsu makan)

        <p><p align ="justify">Setiap orang memiliki kebutuhan nutrisi yang berbeda. Konsultasikan dengan ahli gizi untuk mendiskusikan suplemen apa yang tepat untuk Anda.</p>

        <p><p align ="justify">Semua tips-tips yang disebutkan di atas adalah hal-hal yang cukup mudah dilakukan, tidak peduli seberapa sibuknya Anda. Ingat selalu bahwa kesehatan Anda lebih penting daripada kesibukan Anda.</p>

        <p><p align ="justify">Perlu diingat bahwa kunci dari hidup sehat adalah dedikasi dan motivasi dari diri sendiri untuk memulai hidup sehat. Jadi, meskipun sibuk, jangan lupa untuk tetap memerhatikan menu diet sehat untuk kesehatan Anda.</p>     
           
